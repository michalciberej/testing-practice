export const calculator = {
  'add': function add(numOne, numTwo) {
    return numOne + numTwo
  },
  'substract': function substract(numOne, numTwo) {
    return numOne - numTwo
  },
  'divide': function divide(numOne, numTwo) {
    return numOne / numTwo
  },
  'multiply': function multiply(numOne, numTwo) {
    return numOne * numTwo
  }
}







